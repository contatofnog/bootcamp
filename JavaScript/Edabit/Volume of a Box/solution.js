/* 
  * Volume of a Box - Edabit
  *
  * Create a function that gets an object arguments with 
  * height, width and length of a box and returns the volume of the box.
  *
  * https://edabit.com/challenge/ms3q5GYSpFpwxeFWX
  * github.com/fnogcps
*/

volumeOfBox = a => a.width * a.length * a.height
module.exports = { volumeOfBox }
